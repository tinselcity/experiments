//! ----------------------------------------------------------------------------
//! includes
//! ----------------------------------------------------------------------------
#include <stdio.h>
#include <stdint.h>
//! ----------------------------------------------------------------------------
//! \class Solution
//! ----------------------------------------------------------------------------
class Solution {
public:
    static int hammingWeight(uint32_t n) {
        int l_count = 0;
        while(n)
        {
            if (n & 1) {
                ++l_count;
            }
            n = n >> 1;
        }
        return l_count;
    }
};
//! ----------------------------------------------------------------------------
//! \function main
//! ----------------------------------------------------------------------------
int main(void)
{
#define _TEST(_n) do { \
    printf("NUM 1s: %d :: %d\n", _n, Solution::hammingWeight(_n)); \
} while(0)
    _TEST(0);
    _TEST(3);
    _TEST(8);
    return 0;
}